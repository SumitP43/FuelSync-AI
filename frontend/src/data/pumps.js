export function getPumpById(id) {
  return pumps.find((p) => p.id === id) || null;
}

export function sortPumps(list, sortBy = 'distance') {
  return [...list].sort((a, b) => {
    if (sortBy === 'distance') return a.distance - b.distance;
    if (sortBy === 'waitingTime') return a.waitingTime - b.waitingTime;
    if (sortBy === 'price') return a.price - b.price;
    if (sortBy === 'rating') return b.rating - a.rating;
    return 0;
  });
}

export function getRecommendedPumps() {
  return [...pumps]
    .filter((p) => p.status === 'open')
    .map((p) => ({
      ...p,
      score: Math.round((1 / (p.distance + 0.1)) * 30 + (1 / (p.waitingTime + 1)) * 50 + p.rating * 4),
      reason:
        p.waitingTime <= 5
          ? 'Shortest wait time nearby'
          : p.distance <= 3
            ? 'Closest to your location'
            : p.rating >= 4.5
              ? 'Highest rated station'
              : p.price <= 76
                ? 'Best price in your area'
                : 'Good balance of distance & wait',
    }))
    .sort((a, b) => b.score - a.score)
    .slice(0, 6);
}
